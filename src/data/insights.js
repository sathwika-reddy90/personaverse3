// Derives a "why this fits you" match card per career from the archetype's
// real strengths and recommended skills — presentational only, no score changes.
export const getCareerMatches = (archetypeId) => {
  const data = INSIGHTS[archetypeId] || INSIGHTS.growth;
  const reasonPool = [...data.strengths.map((s) => s.title), ...data.recommendedSkills];
  return data.careerMatches.map((title, i) => ({
    title,
    emoji: CAREER_EMOJI[title] || '💼',
    match: Math.max(74, 94 - i * 4),
    why: [reasonPool[i % reasonPool.length], reasonPool[(i + 2) % reasonPool.length], reasonPool[(i + 4) % reasonPool.length]],
  }));
};

